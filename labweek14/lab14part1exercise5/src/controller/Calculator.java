package controller;

public class Calculator
{
		/**
		 * A method that will get the summation from two numbers
		 * @param number1
		 * @param number2
		 * @param number3
		 * @return summation of the numbers
		 * @return multiplication of the numbers
		 */
	
		public int numberSum(int number1, int number2, int number3)
		{
			return number1 + number2 + number3;
		}
		
		public int numberMultiplication (int number1, int number2, int number3)
		{
			return number1 * number2 * number3;
		}
}